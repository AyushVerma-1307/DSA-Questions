class Solution {
  public:
    void bitManipulation(int num, int i) {
        // your code here
        int get = num >> i-1,d = 1 << i-1, clear, set;
        set = num | d;
        clear = num & (~d);
        get &= 1;
        cout<<get<<" "<<set<<" "<<clear;
    }
};

